#!/usr/bin/node

// declare myAge
let myAge = 1

// declare early-year
let earlyYears = 2
earlyYears = earlyYears * 10.5

// declare laterYears
let laterYears = myAge - 2
laterYears = laterYears * 4

// declare and assigne myAgeInDogYears
let myAgeInDogYears = earlyYears + laterYears

